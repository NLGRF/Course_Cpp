#include <iostream>
using namespace std;
int main()
{
   int i;
   char c;
		
   cout << "Size of variable i : " << sizeof(i) << endl;
   cout << "Size of variable c : " << sizeof(c) << endl;

   cout << "Size of int : " << sizeof(int) << endl;
   cout << "Size of double : " << sizeof(double) << endl;
   cout << "Size of char : " << sizeof(char) << endl;
  return 0;
}
